mp.events.add('playerEnterColshape', (colshape) => {
    if (colshape.onEnter) {
        colshape.onEnter();
    }
});

for (const door of thisInfo.doors) {
    const colshape = mp.colshapes.newSphere(door.position.x, door.position.y, door.position.z, 200);

    colshape.onEnter = async () => {
        const model = mp.game.joaat(door.model);
        const position = door.position;
        const state = door.state;

        const timer = setInterval(() => {
            if (mp.game.object.doesDoorExist(model)) {
                clearInterval(timer);
            }

            mp.game.object.addDoorToSystem(model, model, position.x, position.y, position.z, false, false, false);
            mp.game.object.doorControl(model, position.x, position.y, position.z, state, 0, 0, 0);
        }, 50);
    };

    door.colshape = colshape;
}

