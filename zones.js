function setSafeZone(argument) {
  isInSafeZone = argument;
  mp.gui.execute(`HUD.inSafeZone=${isInSafeZone}`);

  if (mp.players.local.hasVariable('fraction')){
    let frac = mp.players.local.getVariable('fraction');

    if(frac == 6 || frac == 7 || frac == 9 || frac == 18)
      ignoreSafeZone = true;
    else
      ignoreSafeZone = false;
  }
  else {
    ignoreSafeZone = false;
  }
}

